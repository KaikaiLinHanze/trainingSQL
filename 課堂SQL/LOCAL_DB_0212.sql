	GEOGRAPHY_ID = 2
	-- 2.「且」營業額大於300(包含300)
	AND SALES >= 300
)
OR (
	-- 4.「或」營業日介於2018年3月至4月
	STORE_DATE BETWEEN '2018-03-01' AND '2018-04-30'
	-- 3.「且」商店名稱"L"開頭
